import torch

def calc_accel_error(preds, target, sample_wise=True):
    """
    Mean joint-acceleration error (MJA) per frame.

    Parameters
    ----------
    preds : (T, J, 3) torch.Tensor
        Predicted joint coordinates.
    target : (T, J, 3) torch.Tensor
        Ground-truth joint coordinates.
    sample_wise : bool, default=True
        • True  → return a 1-D tensor of length (T-2)  
          (mean over joints for each usable frame)  
        • False → return a 2-D tensor of shape (T-2, J)  
          (per-joint error for each usable frame)

    Returns
    -------
    torch.Tensor
        Acceleration error sequence.
    """
    assert preds.shape == target.shape, f"shape mismatch {preds.shape} vs {target.shape}"
    assert preds.dim() == 3, "expects (T, J, 3)"

    # finite-difference acceleration  a_t = x_{t+1} − 2x_t + x_{t−1}
    accel_gt   = target[:-2] - 2 * target[1:-1] + target[2:]
    accel_pred = preds[:-2] - 2 * preds[1:-1] + preds[2:]

    # L2 error per joint
    accel_err = torch.linalg.norm(accel_pred - accel_gt, dim=-1)      # (T-2, J)

    if sample_wise:
        # average over joints → (T-2,)
        return accel_err.mean(dim=-1)
    else:
        # keep per-joint detail → (T-2, J)
        return accel_err
        
